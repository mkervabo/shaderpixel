#include "libft.h"

void	ft_strclr(char *s)
{
	int		lng;
	int		cmpt;

	if (s)
	{
		lng = ft_strlen(s);
		cmpt = 0;
		while (cmpt < lng)
		{
			s[cmpt] = '\0';
			cmpt++;
		}
	}
}
