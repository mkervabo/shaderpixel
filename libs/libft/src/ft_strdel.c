#include "libft.h"

void	ft_strdel(char **as)
{
	int	cmpt;

	if (as && *as)
	{
		cmpt = 0;
		while (as[0][cmpt])
		{
			as[0][cmpt] = '\0';
			cmpt++;
		}
		ft_memdel((void**)as);
	}
}
