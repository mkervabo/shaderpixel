#include "libft.h"

void	ft_putstr(char const *s)
{
	int		cmpt;

	if (s)
	{
		cmpt = 0;
		while (s[cmpt])
		{
			ft_putchar(s[cmpt]);
			cmpt++;
		}
	}
}
