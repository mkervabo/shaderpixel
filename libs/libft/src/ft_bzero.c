#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cmpt;

	cmpt = 0;
	if (s)
	{
		while (cmpt < n)
		{
			((char *)s)[cmpt] = 0;
			cmpt++;
		}
	}
}
